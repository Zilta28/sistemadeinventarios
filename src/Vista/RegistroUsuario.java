    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        String nombre = txtName.getText();
        String correo = txtCorreoN.getText();
        String contrase침a = txtPass.getText();
        
        
        
        if(nombre.isEmpty()||correo.isEmpty()||contrase침a.isEmpty()){
            JOptionPane.showMessageDialog(null, "DEBE COMPLETAR LOS DATOS");
        }else{ 
            try{
                String consulta = "INSERT INTO usuarios(nombre,correo,pass)VALUES('"+nombre+"','"+correo+"','"+contrase침a+"')";
                PreparedStatement ps = cn.prepareStatement(consulta);
                ps.executeUpdate();
                limpiar();
                JOptionPane.showMessageDialog(null, "Los datos han sido guardados correctamente");
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "No se pudo guardar la informacion"+e);
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed
